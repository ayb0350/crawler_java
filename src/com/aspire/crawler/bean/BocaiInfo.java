package com.aspire.crawler.bean;

public class BocaiInfo {

	
	public int id;
	
	public String bocai_name;
	
	public String bocai_type;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getBocai_name() {
		return bocai_name;
	}

	public void setBocai_name(String bocai_name) {
		this.bocai_name = bocai_name;
	}

	public String getBocai_type() {
		return bocai_type;
	}

	public void setBocai_type(String bocai_type) {
		this.bocai_type = bocai_type;
	}
	
}
